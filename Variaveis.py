#Variavel = container para guardar um valor (string, int, float, boolean)
#uma variavel se comporta como se fosse o valor que ela contem

primeito_nome = "Jose"
comida = "hamburguer"
print(f"Meu nome eh {primeito_nome}")
print(f"Eu gosto muito de {comida}")
#essas são strings, podem incluir numeros

idade = 19
quantidade = 3
print(f"Eu tenho {idade}")
print(f"Estou comprando {quantidade} hamburguers")
#esses são inteiros

dinehiro = 20.30
preco_pedido = 15.20
print(f"Meu saldo na conta do banco eh de RS{dinehiro}")
print(f"O preco do meu pedido foi R${preco_pedido}")
#esses são floats

e_estudante = True
graudado = False
print(f"Voce eh um estudante?: {e_estudante}")
print(f"Voce eh graduado?: {graudado}")
#esses são booleans (valores lógicos)

